 * T - configuración de zona horaria de esta máquina; p. ej. 'MDT'
 * U - segundos desde la época Unix
 * w - día de la semana, numérico, '0' (domingo) a '6' (sábado)
 * Y - año, 4 dígitos; p. ej. '1999'
 * y - año, 2 dígitos; p. ej. '99'
